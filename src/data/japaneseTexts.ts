// 日本語ローマ字入力の練習お題集。すべてひらがな（＋句読点）で記述する。
// 促音・拗音・撥音をひと通り含むようにしてある。

export const JAPANESE_TEXTS: string[] = [
  'きょうもいちにちがんばろう',
  'がっこうへいくみちでねこをみた',
  'しゃしんをとってともだちにおくる',
  'にほんごのれんしゅうはたのしい',
  'あめがふってもかさをわすれない',
  'はやおきしてあさごはんをたべる',
  'でんしゃにのっておおきなまちへいく',
  'おちゃをのみながらほんをよむ',
  'きっぷをかってりょこうにでかける',
  'まいにちすこしずつつづけることがたいせつ',
];

/** お題をランダムに1つ返す。 */
export function randomJapaneseText(): string {
  return JAPANESE_TEXTS[Math.floor(Math.random() * JAPANESE_TEXTS.length)];
}
